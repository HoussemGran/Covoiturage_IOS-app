import UIKit



/** add additional colors to the current UIColor */
extension UIColor {

  @nonobjc class var  cgBlue: UIColor {
    return UIColor(red: 47.0 / 255.0, green: 118.0 / 255.0, blue: 148.0 / 255.0, alpha: 1.0)
  }
    
  @nonobjc class var  coldLips: UIColor {
      return UIColor(red: 144.0 / 255.0, green: 163.0 / 255.0, blue: 243.0 / 255.0, alpha: 1.0)
    }
    
  @nonobjc class var perfectSky: UIColor {
      return UIColor(red: 64.0 / 255.0, green: 147.0 / 255.0, blue: 206.0 / 255.0, alpha: 1.0)
    }
    
  @nonobjc class var lightBlue: UIColor {
      return UIColor(red: 173.0 / 255.0, green: 206.0 / 255.0, blue: 225.0 / 255.0, alpha: 1.0)
    }

}
